package com.commercecoupon.enums;

/**
 * 사용자 권한을 정의하는 열거형
 *
 * USER: 일반 회원
 * VIP: VIP 회원 (추가 할인 혜택 등 특별 권한)
 * ADMIN: 관리자 (상품 관리, 쿠폰 관리, 회원 관리 등 모든 권한)
 */
public enum Role {
    /**
     * 일반 회원
     * - 기본적인 쇼핑 기능 이용 가능
     * - 일반 쿠폰 사용 가능
     * - 장바구니, 주문 기능 이용 가능
     */
    USER,

    /**
     * VIP 회원
     * - 일반 회원의 모든 기능 + VIP 전용 혜택
     * - VIP 전용 쿠폰 사용 가능
     * - 추가 할인 혜택 적용
     * - 우선 구매 권한 등
     */
    VIP,

    /**
     * 관리자
     * - 시스템의 모든 기능에 접근 가능
     * - 상품 등록/수정/삭제
     * - 쿠폰 생성/관리
     * - 회원 관리
     * - 시스템 설정 관리
     */
    ADMIN
}